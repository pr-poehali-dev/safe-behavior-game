export type DisasterType = "flood" | "earthquake";

export interface TheoryCard {
  id: string;
  disaster: DisasterType;
  title: string;
  icon: string;
  content: string;
  tip: string;
}

export interface LevelScenario {
  id: string;
  disaster: DisasterType;
  level: number;
  title: string;
  description: string;
  situation: string;
  options: { text: string; correct: boolean; explanation: string }[];
}

export interface QuizQuestion {
  id: string;
  disaster: DisasterType;
  question: string;
  options: string[];
  correct: number;
  explanation: string;
  points: number;
}

export interface Achievement {
  id: string;
  title: string;
  description: string;
  icon: string;
  condition: (stats: PlayerStats) => boolean;
}

export interface PlayerStats {
  totalPoints: number;
  quizCorrect: number;
  quizTotal: number;
  levelsCompleted: number;
  theoryRead: number;
  streak: number;
}

